#!/usr/bin/env node
"use strict";

/**
 * Build renderer.js by concatenating src/renderer/ modules in order.
 * Files are loaded alphabetically — use numeric prefixes to control order.
 *
 * Usage:
 *   node scripts/build-renderer.js          — one-shot build
 *   node scripts/build-renderer.js --watch  — rebuild on file change
 */

const fs   = require("fs");
const path = require("path");

const SRC = path.join(__dirname, "..", "src", "renderer");
const OUT = path.join(__dirname, "..", "renderer.js");

function build() {
  const files = fs.readdirSync(SRC)
    .filter(f => f.endsWith(".js"))
    .sort();

  const banner = [
    "// ============================================================",
    "// renderer.js — GENERATED FILE",
    "// Edit source modules in src/renderer/, then run:",
    "//   npm run build:renderer",
    "// Do not edit this file directly.",
    "// ============================================================",
    "",
  ].join("\n");

  const parts = files.map(f => {
    const src = fs.readFileSync(path.join(SRC, f), "utf8");
    const header = [
      "",
      `    // ${"=".repeat(58)}`,
      `    // ${f.replace(/^\d+-/, "").replace(".js", "").toUpperCase().replace(/-/g, " ")}  (src/renderer/${f})`,
      `    // ${"=".repeat(58)}`,
      "",
    ].join("\n");
    return header + src;
  });

  fs.writeFileSync(OUT, banner + parts.join("\n"));

  const lines = (banner + parts.join("\n")).split("\n").length;
  const kb    = Math.round(fs.statSync(OUT).size / 1024);
  console.log(`✓ renderer.js  ${lines} lines  ${kb} KB  (${files.length} modules)`);
}

build();

if (process.argv.includes("--watch")) {
  console.log("Watching src/renderer/ for changes…");
  fs.watch(SRC, { recursive: true }, (_, name) => {
    if (!name?.endsWith(".js")) return;
    console.log(`  changed: ${name}`);
    try { build(); } catch (e) { console.error("Build error:", e.message); }
  });
}
